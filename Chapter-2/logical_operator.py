# or : if one value is true than result is true
# and : if all the value is true than only the result will be true (if any one value will be value than also result will be false)
#  not : it gives the opposite result !False = True and !True = False

#truth table of 'or'
print("True or True : ", True or True) 
print("True or False : ", True or False)
print("False or True : ", False or True)
print("False or False : ", False or False)

#truth table of 'and'
print("True and True : ", True and True) 
print("True and False : ", True and False)
print("False and True : ", False and True)
print("False and False : ", False and False)

#truth table of 'not'
print(not(True))
print(not(False))